package Model;

public class TaiKhoan {
	private int maTK;
	private String tenTK;
	private String MK;
	
	public int getMaTK() {
		return maTK;
	}

	public void setMaTK(int maTK) {
		this.maTK = maTK;
	}
	
	public String getTenTK() {
		return tenTK;
	}

	public void setTenTK(String tenTK) {
		this.tenTK = tenTK;
	}

	public String getMK() {
		return MK;
	}

	public void setMK(String MK) {
		this.MK = MK;
	}
	
	
}
